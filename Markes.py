Markes = int(input("Enter the Markes: "))
if Markes >= 90:
    print("Grade: A")
elif Markes >= 80:
    print("Grade: B")
elif Markes >= 70:
    print("Grade: C")
elif Markes >= 60:  
    print("Grade: D")
elif Markes >= 50:
    print("Grade: E")
else:   
    print("Grade: F")
# The code assigns a grade based on the marks entered by the user.
# It uses nested if-else statements to determine the grade based on the conditions.
# The grades are assigned as follows:   
# A: 90 and above, B: 80-89, C: 70-79, D: 60-69, E: 50-59, F: below 50.8